#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	if (!new || !alst)
		return ;
	new->next = *alst;
	*alst = new;
}
