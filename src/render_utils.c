#include "minirt.h"

void	normalize(t_vec3 *v1)
{
	double	mod;

	mod = sqrt(v1->x * v1->x + v1->y * v1->y + v1->z * v1->z);
	v1->x /= mod;
	v1->y /= mod;
	v1->z /= mod;
}
